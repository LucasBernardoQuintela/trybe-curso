function booksOrderedByReleaseYearDesc() {
  // escreva aqui seu código
  let lista = [];
  books.forEach((index) => {
    lista.push(Object.values(index)[4]);
  });
  lista.sort((a, b) => b - a);
  lista.forEach((year, index) => {
    books.forEach((i, id) => {
    const obj = i;
    const release = Object.values(i)[4];
    if (release === year) {
      const oldObj = books[index];
      books[index] = obj;
      books[id] = oldObj;
    }
  })});
  return books;
}

